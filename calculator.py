def add(a, b):
    """두 수의 합을 반환합니다.
    Args: a, b (float): 더할 두 수
    Returns: float: a + b"""
    return a + b

def subtract(a, b):
    """두 수의 차를 반환합니다.
    Args: a, b (float): 첫 번째 수에서 두 번째 수를 뺩니다
    Returns: float: a - b"""
    return a - b

def multiply(a, b):
    """두 수의 곱을 반환합니다.
    Args: a, b (float): 곱할 두 수
    Returns: float: a * b"""
    return a * b
